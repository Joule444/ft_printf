#include "printf.h"

void	print_int(int n)
{
	int	i;

	if (n == -2147483648)
	{
		print_str("-2147483648");
		return ;
	}
	if (n < 0)
	{
		n = n * -1;
		print_char('-');
	}
	i = n % 10;
	if (n > 9)
	{
		print_int(n / 10);
	}
	print_char(i + '0');
}
